# These constants control the overall geometry of the card, meaning where
# the ascii art sits, where the info panel starts and how tall each line
# of text is. Changing W will resize the whole card, while ART_CW is the
# fixed character width used so the ascii art always lines up correctly no
# matter which monospace font actually gets used to render it.
W = 980
ART_X, ART_Y = 30, 140
ART_CW = 3.9          # forced char width (textLength keeps this exact in any font)
ART_LH = ART_CW * 1.72
INFO_X, INFO_Y, INFO_LH = 448, 92, 17.5
